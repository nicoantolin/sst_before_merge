package cl.abcdin.sst.model;


public class ReglaCambioAutomatico {
	private Integer diasTope;
	private Integer numeroFallas;
	private Long precioLimite;
	public Integer getDiasTope() {
		return diasTope;
	}
	public void setDiasTope(Integer diasTope) {
		this.diasTope = diasTope;
	}
	public Integer getNumeroFallas() {
		return numeroFallas;
	}
	public void setNumeroFallas(Integer numeroFallas) {
		this.numeroFallas = numeroFallas;
	}
	public Long getPrecioLimite() {
		return precioLimite;
	}
	public void setPrecioLimite(Long precioLimite) {
		this.precioLimite = precioLimite;
	}
	
	
}
