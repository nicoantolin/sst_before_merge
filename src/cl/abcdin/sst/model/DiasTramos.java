package cl.abcdin.sst.model;

public class DiasTramos {
	private Integer primerTramo;
	private Integer segundoTramo;
	private Integer primerTramoExigencia;
	private Integer segundoTramoExigencia;
	private Integer tercerTramoExigencia;
	
	public Integer getPrimerTramo() {
		return primerTramo;
	}
	public void setPrimerTramo(Integer primerTramo) {
		this.primerTramo = primerTramo;
	}
	
	public Integer getPrimerTramoExigencia() {
		return primerTramoExigencia;
	}
	public void setPrimerTramoExigencia(Integer primerTramoExigencia) {
		this.primerTramoExigencia = primerTramoExigencia;
	}
	public Integer getSegundoTramoExigencia() {
		return segundoTramoExigencia;
	}
	public void setSegundoTramoExigencia(Integer segundoTramoExigencia) {
		this.segundoTramoExigencia = segundoTramoExigencia;
	}
	public Integer getTercerTramoExigencia() {
		return tercerTramoExigencia;
	}
	public void setTercerTramoExigencia(Integer tercerTramoExigencia) {
		this.tercerTramoExigencia = tercerTramoExigencia;
	}
	public Integer getSegundoTramo() {
		return segundoTramo;
	}
	public void setSegundoTramo(Integer segundoTramo) {
		this.segundoTramo = segundoTramo;
	}
	
	
}
