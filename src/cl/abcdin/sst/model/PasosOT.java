package cl.abcdin.sst.model;

public class PasosOT {
	private String facturarElegido;
	private String productoRetirado;
	private String cargoGenerado;
	private String accesoriosRecuperados;
	private String productoRecuperado;
	
	public String getFacturarElegido() {
		return facturarElegido;
	}
	public void setFacturarElegido(String facturarElegido) {
		this.facturarElegido = facturarElegido;
	}
	public String getProductoRetirado() {
		return productoRetirado;
	}
	public void setProductoRetirado(String productoRetirado) {
		this.productoRetirado = productoRetirado;
	}
	public String getCargoGenerado() {
		return cargoGenerado;
	}
	public void setCargoGenerado(String cargoGenerado) {
		this.cargoGenerado = cargoGenerado;
	}
	public String getAccesoriosRecuperados() {
		return accesoriosRecuperados;
	}
	public void setAccesoriosRecuperados(String accesoriosRecuperados) {
		this.accesoriosRecuperados = accesoriosRecuperados;
	}
	public String getProductoRecuperado() {
		return productoRecuperado;
	}
	public void setProductoRecuperado(String productoRecuperado) {
		this.productoRecuperado = productoRecuperado;
	}
	
	
}
