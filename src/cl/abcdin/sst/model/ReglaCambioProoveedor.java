package cl.abcdin.sst.model;

public class ReglaCambioProoveedor {
	private Boolean autorizadoProveedor;
	private String notaProoveedor;
	
	public Boolean getAutorizadoProveedor() {
		return autorizadoProveedor;
	}
	public void setAutorizadoProveedor(Boolean autorizadoProveedor) {
		this.autorizadoProveedor = autorizadoProveedor;
	}
	public String getNotaProoveedor() {
		return notaProoveedor;
	}
	public void setNotaProoveedor(String notaProoveedor) {
		this.notaProoveedor = notaProoveedor;
	}
}
