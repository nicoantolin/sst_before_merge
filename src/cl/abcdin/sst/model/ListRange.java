package cl.abcdin.sst.model;

import java.util.List;

public class ListRange {
	private Integer total;
	private List<?> rows;
	
	public Integer getTotal() {
		return total;
	}
	public void setTotal(Integer total) {
		this.total = total;
	}
	public List<?> getRows() {
		return rows;
	}
	public void setRows(List<?> data) {
		this.rows = data;
	}
}
