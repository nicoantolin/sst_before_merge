package cl.abcdin.sst.model.filters;

public class FilterOTMobile {
	private String codigoBarra;
	private String codigoBarraAccesorio;
	private Long idDespachoInterno;
	private Long idOT;
	
	public String getCodigoBarra() {
		return codigoBarra;
	}
	public void setCodigoBarra(String codigoBarra) {
		this.codigoBarra = codigoBarra;
	}
	public Long getIdDespachoInterno() {
		return idDespachoInterno;
	}
	public void setIdDespachoInterno(Long idDespachoInterno) {
		this.idDespachoInterno = idDespachoInterno;
	}
	public Long getIdOT() {
		return idOT;
	}
	public void setIdOT(Long idOT) {
		this.idOT = idOT;
	}
	public String getCodigoBarraAccesorio() {
		return codigoBarraAccesorio;
	}
	public void setCodigoBarraAccesorio(String codigoBarraAccesorio) {
		this.codigoBarraAccesorio = codigoBarraAccesorio;
	}
}
